ASSUME CS: CODE,DS:DATA
DATA SEGMENT
NUMS DW 1H,2H,3H,4H,5H
COUNT EQU 05H
DATA ENDS
CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
MOV AX,0000H 
MOV DL,COUNT-1
BACK1: 
MOV CL,DL
MOV SI,OFFSET NUMS
BACK: MOV AX,[SI]
CMP AX,[SI+2]
JNC GO
XCHG AX,[SI+2]
MOV [SI],AX
GO:
INC SI
INC SI
LOOP BACK
DEC DL
JNZ BACK1
MOV AH,4CH
INT  21H
CODE ENDS 
END START
END
